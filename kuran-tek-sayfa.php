    const counts = { meali: 0, kk: 0 };

    function animateCount(el, target, duration = 800, startFrom) {
      const start = startFrom ?? 0;
      const startTime = performance.now();
      function step(now) {
        const elapsed = now - startTime;
        const progress = Math.min(elapsed / duration, 1);
        const ease = 1 - Math.pow(1 - progress, 2);
        const val = Math.floor(start + (target - start) * ease);
        el.textContent = val;
        if (progress < 1) requestAnimationFrame(step);
      }
      requestAnimationFrame(step);
    }

    function addOneAnimation(el) {
      const plus = document.createElement('span');
      plus.className = 'count-plus-one';
      plus.textContent = '+1';
      el.appendChild(plus);
      setTimeout(() => plus.remove(), 800);
    }

    function fetchAndShowCounts(animate = true) {
